package com.ritesh.SpringBatch.MultiThreadedJob;

public class Record {

	private int ID;
	private String NAME;
	
	public Record() {
		
	}
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getNAME() {
		return NAME;
	}
	public void setNAME(String nAME) {
		NAME = nAME;
	}
	
	
}
